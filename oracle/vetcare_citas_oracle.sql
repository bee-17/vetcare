-- ----------------------------------------------------------------------------
-- SP 1: sp_registrar_cita
-- Problema que resuelve: evita que dos citas se agenden con el mismo
-- veterinario, en la misma fecha y hora (doble reserva), centralizando la
-- validacion en la base de datos en lugar de dejarla solo en la aplicacion.
-- ----------------------------------------------------------------------------
CREATE OR REPLACE PROCEDURE sp_registrar_cita (
    p_id_mascota      IN  citas.id_mascota%TYPE,
    p_id_veterinario  IN  citas.id_veterinario%TYPE,
    p_fecha_cita      IN  citas.fecha_cita%TYPE,
    p_hora_cita       IN  citas.hora_cita%TYPE,
    p_motivo          IN  citas.motivo%TYPE,
    p_id_cita_out     OUT citas.id_cita%TYPE
) IS
    v_ocupado NUMBER;
    v_vet_activo NUMBER;
BEGIN
    -- Validar que el veterinario exista y este activo
    SELECT COUNT(*) INTO v_vet_activo
    FROM veterinarios
    WHERE id_veterinario = p_id_veterinario AND activo = 1;

    IF v_vet_activo = 0 THEN
        RAISE_APPLICATION_ERROR(-20001, 'El veterinario no existe o no esta activo.');
    END IF;

    -- Validar que no exista otra cita activa con el mismo veterinario,
    -- fecha y hora (doble reserva)
    SELECT COUNT(*) INTO v_ocupado
    FROM citas
    WHERE id_veterinario = p_id_veterinario
      AND fecha_cita = p_fecha_cita
      AND hora_cita = p_hora_cita
      AND estado IN ('PENDIENTE', 'CONFIRMADA');

    IF v_ocupado > 0 THEN
        RAISE_APPLICATION_ERROR(-20002, 'El veterinario ya tiene una cita en esa fecha y hora.');
    END IF;

    INSERT INTO citas (id_mascota, id_veterinario, fecha_cita, hora_cita, motivo, estado)
    VALUES (p_id_mascota, p_id_veterinario, p_fecha_cita, p_hora_cita, p_motivo, 'PENDIENTE')
    RETURNING id_cita INTO p_id_cita_out;

    COMMIT;
END sp_registrar_cita;
/


-- ----------------------------------------------------------------------------
-- SP 2: sp_actualizar_estado_cita
-- Problema que resuelve: aplica las reglas del negocio sobre los cambios de
-- estado de una cita (maquina de estados), evitando que una cita ya
-- "ATENDIDA" o "CANCELADA" (estados finales) vuelva a modificarse por error.
-- ----------------------------------------------------------------------------
CREATE OR REPLACE PROCEDURE sp_actualizar_estado_cita (
    p_id_cita     IN citas.id_cita%TYPE,
    p_nuevo_estado IN citas.estado%TYPE
) IS
    v_estado_actual citas.estado%TYPE;
BEGIN
    SELECT estado INTO v_estado_actual
    FROM citas
    WHERE id_cita = p_id_cita;

    IF v_estado_actual IN ('ATENDIDA', 'CANCELADA') THEN
        RAISE_APPLICATION_ERROR(-20003,
            'No se puede modificar una cita en estado final (' || v_estado_actual || ').');
    END IF;

    UPDATE citas
    SET estado = p_nuevo_estado
    WHERE id_cita = p_id_cita;

    COMMIT;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20004, 'La cita indicada no existe.');
END sp_actualizar_estado_cita;
/


-- ============================================================================
-- 3. FUNCIONES
-- ============================================================================

-- ----------------------------------------------------------------------------
-- FN 1: fn_citas_pendientes_mascota
-- Problema que resuelve: entrega rapidamente cuantas citas pendientes tiene
-- una mascota, informacion usada por el reporte "citas pendientes por
-- mascota" y por alertas del panel administrativo.
-- ----------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION fn_citas_pendientes_mascota (
    p_id_mascota IN citas.id_mascota%TYPE
) RETURN NUMBER IS
    v_total NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_total
    FROM citas
    WHERE id_mascota = p_id_mascota
      AND estado = 'PENDIENTE';

    RETURN v_total;
END fn_citas_pendientes_mascota;
/


-- ----------------------------------------------------------------------------
-- FN 2: fn_disponibilidad_veterinario
-- Problema que resuelve: permite consultar, antes de agendar, si un
-- veterinario esta disponible en una fecha/hora especifica, evitando que el
-- usuario intente registrar una cita que sp_registrar_cita rechazara.
-- ----------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION fn_disponibilidad_veterinario (
    p_id_veterinario IN citas.id_veterinario%TYPE,
    p_fecha_cita     IN citas.fecha_cita%TYPE,
    p_hora_cita      IN citas.hora_cita%TYPE
) RETURN VARCHAR2 IS
    v_ocupado NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_ocupado
    FROM citas
    WHERE id_veterinario = p_id_veterinario
      AND fecha_cita = p_fecha_cita
      AND hora_cita = p_hora_cita
      AND estado IN ('PENDIENTE', 'CONFIRMADA');

    IF v_ocupado > 0 THEN
        RETURN 'OCUPADO';
    ELSE
        RETURN 'DISPONIBLE';
    END IF;
END fn_disponibilidad_veterinario;
/


-- ============================================================================
-- 4. TRIGGERS
-- ============================================================================

-- ----------------------------------------------------------------------------
-- TRIGGER 1: trg_valida_fecha_cita
-- Problema que resuelve: garantiza la integridad de los datos impidiendo que
-- se registre o reprograme una cita con fecha anterior a hoy.
-- ----------------------------------------------------------------------------
CREATE OR REPLACE TRIGGER trg_valida_fecha_cita
BEFORE INSERT OR UPDATE OF fecha_cita ON citas
FOR EACH ROW
BEGIN
    IF :NEW.fecha_cita < TRUNC(SYSDATE) THEN
        RAISE_APPLICATION_ERROR(-20010, 'No se puede agendar una cita en una fecha pasada.');
    END IF;
END trg_valida_fecha_cita;
/


-- ----------------------------------------------------------------------------
-- TRIGGER 2: trg_auditoria_estado_cita
-- Problema que resuelve: mantiene un historial de auditoria de cada cambio
-- de estado de una cita (quien y cuando la modifico), sin depender de que
-- la aplicacion lo registre manualmente.
-- ----------------------------------------------------------------------------
CREATE OR REPLACE TRIGGER trg_auditoria_estado_cita
AFTER UPDATE OF estado ON citas
FOR EACH ROW
WHEN (OLD.estado != NEW.estado)
BEGIN
    INSERT INTO auditoria_citas (id_cita, estado_anterior, estado_nuevo, usuario_bd, fecha_cambio)
    VALUES (:NEW.id_cita, :OLD.estado, :NEW.estado, USER, SYSDATE);
END trg_auditoria_estado_cita;
/


