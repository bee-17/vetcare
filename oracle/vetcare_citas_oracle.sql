DROP TABLE auditoria_citas CASCADE CONSTRAINTS;
DROP TABLE citas CASCADE CONSTRAINTS;
DROP TABLE mascotas CASCADE CONSTRAINTS;
DROP TABLE veterinarios CASCADE CONSTRAINTS;
DROP TABLE clientes CASCADE CONSTRAINTS;

CREATE TABLE clientes (
    id_cliente      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombres         VARCHAR2(100) NOT NULL,
    apellidos       VARCHAR2(100) NOT NULL,
    tipo_documento  VARCHAR2(10)  DEFAULT 'DNI',
    num_documento   VARCHAR2(20)  NOT NULL UNIQUE,
    telefono        VARCHAR2(20),
    email           VARCHAR2(150)
);

CREATE TABLE veterinarios (
    id_veterinario  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombres         VARCHAR2(100) NOT NULL,
    apellidos       VARCHAR2(100) NOT NULL,
    especialidad    VARCHAR2(100),
    telefono        VARCHAR2(20),
    email           VARCHAR2(150),
    activo          NUMBER(1) DEFAULT 1 CHECK (activo IN (0, 1))
);

CREATE TABLE mascotas (
    id_mascota          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_cliente          NUMBER NOT NULL,
    nombre              VARCHAR2(100) NOT NULL,
    especie             VARCHAR2(50),
    raza                VARCHAR2(100),
    sexo                CHAR(1) DEFAULT 'M' CHECK (sexo IN ('M', 'H')),
    fecha_nacimiento    DATE,
    CONSTRAINT fk_mascota_cliente FOREIGN KEY (id_cliente)
        REFERENCES clientes(id_cliente) ON DELETE CASCADE
);

CREATE TABLE citas (
    id_cita         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_mascota      NUMBER NOT NULL,
    id_veterinario  NUMBER NOT NULL,
    fecha_cita      DATE NOT NULL,
    hora_cita       VARCHAR2(5) NOT NULL,      -- formato 'HH24:MI'
    motivo          VARCHAR2(200),
    estado          VARCHAR2(15) DEFAULT 'PENDIENTE'
        CHECK (estado IN ('PENDIENTE', 'CONFIRMADA', 'ATENDIDA', 'CANCELADA')),
    creado_en       DATE DEFAULT SYSDATE,
    CONSTRAINT fk_cita_mascota FOREIGN KEY (id_mascota)
        REFERENCES mascotas(id_mascota) ON DELETE CASCADE,
    CONSTRAINT fk_cita_veterinario FOREIGN KEY (id_veterinario)
        REFERENCES veterinarios(id_veterinario)
);

-- Tabla de auditoria: registra cada cambio de estado de una cita
CREATE TABLE auditoria_citas (
    id_auditoria    NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_cita         NUMBER NOT NULL,
    estado_anterior VARCHAR2(15),
    estado_nuevo    VARCHAR2(15),
    usuario_bd      VARCHAR2(50),
    fecha_cambio    DATE DEFAULT SYSDATE,
    CONSTRAINT fk_auditoria_cita FOREIGN KEY (id_cita)
        REFERENCES citas(id_cita) ON DELETE CASCADE
);


