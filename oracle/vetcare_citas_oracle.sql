-- ----------------------------------------------------------------------------
-- PACKAGE SPECIFICATION
-- ----------------------------------------------------------------------------
CREATE OR REPLACE PACKAGE pkg_gestion_citas IS

    -- Reprograma una cita existente (cambia fecha/hora) validando
    -- disponibilidad del veterinario en el nuevo horario.
    PROCEDURE reprogramar_cita (
        p_id_cita        IN citas.id_cita%TYPE,
        p_nueva_fecha    IN citas.fecha_cita%TYPE,
        p_nueva_hora     IN citas.hora_cita%TYPE
    );

    -- Cancela una cita y registra el motivo en el campo "motivo"
    PROCEDURE cancelar_cita (
        p_id_cita     IN citas.id_cita%TYPE,
        p_motivo_cancelacion IN VARCHAR2
    );

    -- Retorna el total de citas registradas para una fecha especifica
    FUNCTION total_citas_dia (
        p_fecha IN DATE
    ) RETURN NUMBER;

    -- Retorna un cursor con las citas de un veterinario (para reportes)
    FUNCTION citas_por_veterinario (
        p_id_veterinario IN citas.id_veterinario%TYPE
    ) RETURN SYS_REFCURSOR;

END pkg_gestion_citas;
/

-- ----------------------------------------------------------------------------
-- PACKAGE BODY
-- ----------------------------------------------------------------------------
CREATE OR REPLACE PACKAGE BODY pkg_gestion_citas IS

    PROCEDURE reprogramar_cita (
        p_id_cita        IN citas.id_cita%TYPE,
        p_nueva_fecha    IN citas.fecha_cita%TYPE,
        p_nueva_hora     IN citas.hora_cita%TYPE
    ) IS
        v_id_veterinario citas.id_veterinario%TYPE;
        v_disponibilidad VARCHAR2(15);
    BEGIN
        SELECT id_veterinario INTO v_id_veterinario
        FROM citas WHERE id_cita = p_id_cita;

        v_disponibilidad := fn_disponibilidad_veterinario(v_id_veterinario, p_nueva_fecha, p_nueva_hora);

        IF v_disponibilidad = 'OCUPADO' THEN
            RAISE_APPLICATION_ERROR(-20020, 'El veterinario ya tiene otra cita en ese nuevo horario.');
        END IF;

        UPDATE citas
        SET fecha_cita = p_nueva_fecha,
            hora_cita  = p_nueva_hora,
            estado     = 'PENDIENTE'
        WHERE id_cita = p_id_cita;

        COMMIT;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            RAISE_APPLICATION_ERROR(-20021, 'La cita indicada no existe.');
    END reprogramar_cita;


    PROCEDURE cancelar_cita (
        p_id_cita     IN citas.id_cita%TYPE,
        p_motivo_cancelacion IN VARCHAR2
    ) IS
    BEGIN
        sp_actualizar_estado_cita(p_id_cita, 'CANCELADA');

        UPDATE citas
        SET motivo = motivo || ' | Cancelada: ' || p_motivo_cancelacion
        WHERE id_cita = p_id_cita;

        COMMIT;
    END cancelar_cita;


    FUNCTION total_citas_dia (
        p_fecha IN DATE
    ) RETURN NUMBER IS
        v_total NUMBER;
    BEGIN
        SELECT COUNT(*) INTO v_total
        FROM citas
        WHERE fecha_cita = TRUNC(p_fecha);

        RETURN v_total;
    END total_citas_dia;


    FUNCTION citas_por_veterinario (
        p_id_veterinario IN citas.id_veterinario%TYPE
    ) RETURN SYS_REFCURSOR IS
        v_cursor SYS_REFCURSOR;
    BEGIN
        OPEN v_cursor FOR
            SELECT c.id_cita, m.nombre AS mascota, c.fecha_cita, c.hora_cita, c.estado
            FROM citas c
            JOIN mascotas m ON m.id_mascota = c.id_mascota
            WHERE c.id_veterinario = p_id_veterinario
            ORDER BY c.fecha_cita DESC;

        RETURN v_cursor;
    END citas_por_veterinario;

END pkg_gestion_citas;
/


